def isValidSudoku(board):
    res = []
    for i, row in enumerate(board):
        for j, x in enumerate(row):
            if x != '.':
                res += [(i, x), (x, j), (i // 3, j // 3, x)]
    print(res)
    print(set(res))

    return len(res) == len(set(res))


